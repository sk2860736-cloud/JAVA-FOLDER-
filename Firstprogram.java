package LECTURE1;

public class Firstprogram {
    public static void main(String[] args) {
        // INTEGER --> BYTE, SHORT, INT, LONG 
        byte b = 8;
        short s = 10;
        int  i = 678;
        long l = 9876;

        // RAEL NUMBER --> FLOAT, DOUBLE 
        float f = 5.6f;
        double d = 546.56;

        // CHARACTER --> CHAR
        char c = 'a';

        // BOOLEAN --> BOOL, TRUE FALSE 
        boolean bool = true; 

        System.out.println( "Integer value is : " +b+","  + s+"," + i+"," + l );
        System.out.println( "Real number is : " +f+","  + d);
        System.out.println( "Character is : " +c);
        System.out.println( "Boolean is  : " +bool);
    }
    

}